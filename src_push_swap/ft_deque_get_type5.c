static void	_front_is_size_1(t_deque *dq, int a[5])
{
	a[0] = dq->val[dq->front];
	a[1] = dq->val[0];
	a[2] = dq->val[1];
	a[3] = dq->val[2];
	a[4] = dq->val[3];
}

static void	_front_is_size_2(t_deque *dq, int a[4])
{
	a[0] = dq->val[dq->front];
	a[1] = dq->val[dq->front + 1];
	a[2] = dq->val[0];
	a[3] = dq->val[1];
	a[4] = dq->val[2];
}

static void	_front_is_size_3(t_deque *dq, int a[4])
{
	a[0] = dq->val[dq->front];
	a[1] = dq->val[dq->front + 1];
	a[2] = dq->val[dq->front + 2];
	a[3] = dq->val[0];
	a[4] = dq->val[1];
}

static void	_front_is_size_4(t_deque *dq, int a[4])
{
	a[0] = dq->val[dq->front];
	a[1] = dq->val[dq->front + 1];
	a[2] = dq->val[dq->front + 2];
	a[3] = dq->val[dq->front + 3];
	a[4] = dq->val[0];
}

int	ft_deque_get_type5(t_deque *dq)
{
	int	a[5];

	if (ft_deque_get_size(dq, dq->front, dq->back) < 5)
		return (-1);
	if (dq->front == dq->size - 1)
		_front_is_size_1(dq, a);
	else if (dq->front == dq->size - 2)
		_front_is_size_2(dq, a);
	else if (dq->front == dq->size - 3)
		_front_is_size_3(dq, a);
	else if (dq->front == dq->size - 4)
		_front_is_size_4(dq, a);
	else
	{
		a[0] = dq->val[dq->front];
		a[1] = dq->val[dq->front + 1];
		a[2] = dq->val[dq->front + 2];
		a[3] = dq->val[dq->front + 3];
		a[4] = dq->val[dq->front + 4];
	}
	return (0);
}
