#include "libft.h"

/*
** 0 1 -> 0
** 1 0 -> 1 (sa)
*/

int	ft_array_sort2(int *a, t_cmp *cmp)
{
	if (cmp(&a[0], &a[1]) > 0)
	{
		a[0] = a[0] - a[1];
		a[1] = a[0] + a[1];
		a[0] = a[1] - a[0];
		return (1);
	}
	return (0);
}
